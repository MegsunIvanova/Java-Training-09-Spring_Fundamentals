//Comparison Operators examples
var result = 1 == '1' //true
console.log(result)

result = 1 === '1' //false
console.log(result)

result = 1 != '1' //false
console.log(result)

result = 1 !== '1' //true
console.log(result)

//ternary operator
let a = true ? "foo" : "bar"
console.log(a) //foo

a = false ? "foo" : "bar"
console.log(a) //bar